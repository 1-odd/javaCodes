public class TypeCasting {
    public static void main(String args[]){

        /*
           it is used when LOSSY ERROR occurs
           
           we can also applied it on character values it will give the ASCI value of that char value as output


         */
           
        float a = 15.77f;
        int b = (int) a;
        System.out.println(b);



        // TYPECASTING IN  CHAR VALUES


        char ch = 'd';
        int number = ch;
        System.out.println(number);


        







    }
    
}
